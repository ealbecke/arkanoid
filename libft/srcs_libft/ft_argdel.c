#include "libft.h"

void				ft_argdel(char ***arg)
{
	int					i;

	if (arg && *arg)
	{
		i = -1;
		while ((*arg)[++i])
			free((*arg)[i]);
		free(*arg);
		*arg = NULL;
	}
}
